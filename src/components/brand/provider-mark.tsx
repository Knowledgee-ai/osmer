interface ProviderMarkProps {
  provider: ModelProviderId;
  size?: number;
  className?: string;
  /** Paint with the provider's signature colour. Default true. When
   *  false, inherits currentColor — useful in monochrome contexts. */
  brandColor?: boolean;
}

export function ProviderMark({
  provider,
  size = 14,
  className,
  brandColor = true,
}: ProviderMarkProps) {
  const def = MARKS[provider];
  if (!def) return null;
  const fill = brandColor ? PROVIDER_COLORS[provider] || "currentColor" : "currentColor";
  return (
    <svg
      width={size}
      height={size}
      viewBox={def.viewBox}
      className={className}
      aria-hidden="true"
      fill={fill}
      style={{ flexShrink: 0 }}
    >
      {def.paths.map((d, i) => (
        <path key={i} d={d} />
      ))}
    </svg>
  );
}
